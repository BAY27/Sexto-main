<?php
require_once('cls_conexion.model.php');
class Clase_Computadoras
{
    public function todos()
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `computadoras`";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function uno($computadoraId)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `computadoras` WHERE computadoraId=$computadoraId";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function insertar($tipocomputadora, $modelo, $nserie, $marca,  $precio)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "INSERT INTO `computadoras`(`tipocomputadora`, `modelo`, `nserie`, `marca`, `precio`)VALUES ( '$modelo', '$nserie', '$tipocomputadora', $marca, '$precio')";
          
            $result = mysqli_query($con, $cadena);
            return 'ok';
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function actualizar($computadoraId, $tipocomputadora, $modelo, $nserie, $marca, $precio)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "UPDATE `computadoras` SET `computadoraId`='$computadoraId', `tipocomputadora`='$tipocomputadora', `modelo`='$modelo', `nserie`='$nserie', `marca`='$marca, `precio`=$precio' WHERE `computadoraId`='$computadoraId'";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function eliminar($computadoraId)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "delete from computadoras where computadoraId=$computadoraId";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }



}
